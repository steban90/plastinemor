<p class="text-justify ">
    <strong>La materia prima que usa Plastinemor S.A.S para la producción de capuchones para empaque de flores de exportación son:</strong>
</p>

<dl>
    <dt>Polipropileno Biorentado Transparente</dt>
    <dd class="text-justify small text-indent-100">
        Este tipo de plástico posee dos capas de polipropileno estirados biaxialmente, lo que quiere decir que una capa es puesta en forma transversal sobre una capa de OPP y otra capa es estirada en forma longitudinal en la otra cara del OPP. Según la aplicación que se desee es un producto que da las siguientes propiedades: Excelente calidad y brillo, Alta resistencia y rigidez, resistencia a humedad, calor u otros factores
    </dd>
    <dt>Polipropileno Mono-Orientado Transparente</dt>
    <dd class="text-justify small text-indent-100">
        Material para la elaboración de capuchones, lamina para envoltura de Rosas, y elaboración de bolsas plásticas.<br />
        Es transparente, permite sobretodo microperforarse, es maleable y dúctil y se trabaja en capuchones en calibres delgados        
    </dd>
    <dt>Polipropileno Perlado</dt>
    <dd class="text-justify small text-indent-100">
        Este tipo de plástico posee un acabado brillante con un recubrimiento para garantizar una buena impresión. Posee un adhesivo de alta adherencia en sustratos apolares tales como el Polietileno y Polipropileno.
    </dd>
    <dt>Polipropileno Metalizado</dt>
    <dd class="text-justify small text-indent-100">
        Este producto tiene recubrimiento especial para facilitar la impresión, pero la calidad de la misma es responsabilidad del cliente, quien deberá validar con su proveedor de tintas.
    </dd>
    <dt>Polipropileno Microperforado</dt>
    <dd class="text-justify small text-indent-100">
        Es una película de plástico de multicapa, transpirable y Micro perforado.<br />
        Especialmente utilizado para el embalaje y retractilado de las flores teniendo en cuenta las propiedades de transpiración de las mismas.
    </dd>
</dl>
